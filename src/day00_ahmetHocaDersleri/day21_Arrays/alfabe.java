package day00_ahmetHocaDersleri.day21_Arrays;

public class alfabe {
    public static void main(String[] args) {
          /*
        Alfabeyi konsola yazdırın.
    örnek:
    a b c .. .. .. .. y z
         ipucu: char kullanmak iyi bir fikir olabilir :wink:

         */


        for (int i = 'a'; i <= 'z'; i++) {
            System.out.print((char) i + " ");

        }
    }
}
